% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Memanggil variabel img pada handles
x = handles.img;

% Ekstraksi Ciri Warna RGB
fR=x(:,:,1);
fG=x(:,:,2);
fB=x(:,:,3);

% Mengetahui Tingkat Masing-masing Piksel RGB
fr=mean(mean(fR));
fb=mean(mean(fG));
fc=mean(mean(fB));
frr=fr/255;
fbb=fb/255;
fcc=fc/255;

% Menampilkan Hasil Nilai Normalisasi RGB
set(handles.edit2,'String',frr)
set(handles.edit3,'String',fbb)
set(handles.edit4,'String',fcc)

% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Menu "Browse File"
[nama_file, nama_folder] = uigetfile('*.jpg');

% Jika ada file yang dipilih, maka perintah di bawah akan dieksekusi
if ~isequal(nama_file, 0)
    % Membaca File Citra RGB
    img = imread(fullfile(nama_folder, nama_file));
    % Menampilkan Citra Pada Axes 1
    axes(handles.axes1)
    imshow(img)
    % Menampilkan Nama File pada Edit Text
    set(handles.edit1,'String', nama_file)
    % Menyimpan variabel img pada handles
    handles.img = img;
    guidata(hObject, handles)
else
    % Jika tidak ada file yang dipilih, maka akan kembali
    return
end


